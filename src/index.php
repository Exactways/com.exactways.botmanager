<?php
class PhpUnderControl_Example_Math
{
    /**
* Adds the two given values.
*
* @param integer $v1 Value one.
* @param integer $v2 Value two.
*
* @return integer.
*/
    public function add($v1, $v2)
    {
        return ($v1 + $v2);
    }

    /**
* Subtract param two from param one
*
* @param integer $v1 Value one.
* @param integer $v2 Value two.
*
* @return integer.
*/
    public function sub($v1, $v2)
    {
        return ($v1 - $v2);
    }

    
}